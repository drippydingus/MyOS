/* ── Mode 13h entry ──────────────────────────────────────── */

static void set_mode13h(void) {
    /* INT 10h AX=0013h via inline — but we're in protected mode.
       Use direct port I/O to the BIOS-equivalent VGA register sequence. */
    /* Miscellaneous Output */
    outb(0x3C2, 0x63);

    /* Sequencer */
    static const uint8_t seq[][2] = {
        {0x00,0x03},{0x01,0x01},{0x02,0x0F},{0x03,0x00},{0x04,0x0E}
    };
    for (int i = 0; i < 5; i++) {
        outb(0x3C4, seq[i][0]);
        outb(0x3C5, seq[i][1]);
    }

    /* CRTC — unlock then program */
    outb(0x3D4, 0x11); outb(0x3D5, inb(0x3D5) & 0x7F);
    static const uint8_t crtc[] = {
        0x5F,0x4F,0x50,0x82,0x54,0x80,0xBF,0x1F,
        0x00,0x41,0x00,0x00,0x00,0x00,0x00,0x00,
        0x9C,0x0E,0x8F,0x28,0x40,0x96,0xB9,0xA3,0xFF
    };
    for (int i = 0; i < 25; i++) { outb(0x3D4, i); outb(0x3D5, crtc[i]); }

    /* Graphics controller */
    static const uint8_t gc[][2] = {
        {0x00,0x00},{0x01,0x00},{0x02,0x00},{0x03,0x00},
        {0x04,0x00},{0x05,0x40},{0x06,0x05},{0x07,0x0F},{0x08,0xFF}
    };
    for (int i = 0; i < 9; i++) { outb(0x3CE, gc[i][0]); outb(0x3CF, gc[i][1]); }

    /* Attribute controller */
    static const uint8_t ac[] = {
        0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
        0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,0x0F,
        0x41,0x00,0x0F,0x00,0x00
    };
    inb(0x3DA); /* reset flip-flop */
    for (int i = 0; i < 21; i++) { outb(0x3C0, i); outb(0x3C0, ac[i]); }
    outb(0x3C0, 0x20);
}

/* ── Palette ──────────────────────────────────────────────── */

static void set_palette_entry(uint8_t idx, uint8_t r, uint8_t g, uint8_t b) {
    outb(0x3C8, idx);
    outb(0x3C9, r >> 2);   /* VGA uses 6-bit components */
    outb(0x3C9, g >> 2);
    outb(0x3C9, b >> 2);
}

static void setup_palette(void) {
    set_palette_entry(COL_BLACK,      0,   0,   0);
    set_palette_entry(COL_WHITE,    255, 255, 255);
    set_palette_entry(COL_RED,      220,  40,  40);
    set_palette_entry(COL_GREEN,     40, 200,  40);
    set_palette_entry(COL_BLUE,      40,  80, 220);
    set_palette_entry(COL_CYAN,       0, 210, 210);
    set_palette_entry(COL_MAGENTA,  210,   0, 210);
    set_palette_entry(COL_YELLOW,   255, 220,   0);
    set_palette_entry(COL_DARK_GREY, 60,  60,  60);
    set_palette_entry(COL_LIGHT_GREY,180,180, 180);
    set_palette_entry(COL_ORANGE,   255, 140,   0);
    set_palette_entry(COL_PINK,     255, 100, 180);
    set_palette_entry(COL_DARK_BLUE, 20,  20, 120);
    set_palette_entry(COL_DARK_GREEN,20,  90,  20);
    set_palette_entry(COL_BROWN,    140,  80,  20);
    /* 16-31: greyscale ramp */
    for (int i = 0; i < 16; i++) {
        uint8_t v = i * 17;
        set_palette_entry(16 + i, v, v, v);
    }
    /* 32-63: blue gradient for desktop */
    for (int i = 0; i < 32; i++) {
        set_palette_entry(32+i, 20+i, 40+i*2, 80+i*3 > 255 ? 255 : 80+i*3);
    }
}

