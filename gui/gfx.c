/* ── gfx_init ─────────────────────────────────────────────── */

void gfx_init(void) {
    set_mode13h();
    setup_palette();
    gfx_clear(COL_BLACK);
    gfx_flip();
}

/* ── Primitives ───────────────────────────────────────────── */

void gfx_clear(uint8_t col) {
    for (int y = 0; y < SCREEN_H; y++)
        for (int x = 0; x < SCREEN_W; x++)
            back_buf[y][x] = col;
}

void gfx_flip(void) {
    volatile uint8_t* dst = VRAM;
    uint8_t* src = (uint8_t*)back_buf;
    for (int i = 0; i < SCREEN_W * SCREEN_H; i++)
        dst[i] = src[i];
}

void gfx_putpixel(int x, int y, uint8_t col) {
    if ((unsigned)x < SCREEN_W && (unsigned)y < SCREEN_H)
        back_buf[y][x] = col;
}

void gfx_hline(int x0, int x1, int y, uint8_t col) {
    if (x0 > x1) { int t=x0; x0=x1; x1=t; }
    if ((unsigned)y >= SCREEN_H) return;
    if (x0 < 0) x0 = 0;
    if (x1 >= SCREEN_W) x1 = SCREEN_W-1;
    for (int x = x0; x <= x1; x++) back_buf[y][x] = col;
}

void gfx_vline(int x, int y0, int y1, uint8_t col) {
    if (y0 > y1) { int t=y0; y0=y1; y1=t; }
    if ((unsigned)x >= SCREEN_W) return;
    if (y0 < 0) y0 = 0;
    if (y1 >= SCREEN_H) y1 = SCREEN_H-1;
    for (int y = y0; y <= y1; y++) back_buf[y][x] = col;
}

void gfx_rect(int x, int y, int w, int h, uint8_t col) {
    gfx_hline(x, x+w-1, y,       col);
    gfx_hline(x, x+w-1, y+h-1,   col);
    gfx_vline(x,     y, y+h-1,   col);
    gfx_vline(x+w-1, y, y+h-1,   col);
}

void gfx_fill_rect(int x, int y, int w, int h, uint8_t col) {
    for (int dy = 0; dy < h; dy++)
        gfx_hline(x, x+w-1, y+dy, col);
}

void gfx_circle(int cx, int cy, int r, uint8_t col) {
    int x=0, y=r, d=3-2*r;
    while (x<=y) {
        gfx_putpixel(cx+x,cy+y,col); gfx_putpixel(cx-x,cy+y,col);
        gfx_putpixel(cx+x,cy-y,col); gfx_putpixel(cx-x,cy-y,col);
        gfx_putpixel(cx+y,cy+x,col); gfx_putpixel(cx-y,cy+x,col);
        gfx_putpixel(cx+y,cy-x,col); gfx_putpixel(cx-y,cy-x,col);
        d += (d<0) ? 4*x+6 : 4*(x-y--)+10;
        x++;
    }
}

static int isqrt_i(int n) {
    if (n <= 0) return 0;
    int x = n, y = 1;
    while (x > y) { x = (x+y)/2; y = n/x; }
    return x;
}

void gfx_fill_circle(int cx, int cy, int r, uint8_t col) {
    for (int y = -r; y <= r; y++) {
        int dx = isqrt_i(r*r - y*y);
        gfx_hline(cx - dx, cx + dx, cy + y, col);
    }
}

/* ── Font rendering ───────────────────────────────────────── */

void gfx_putchar(int x, int y, char c, uint8_t fg, uint8_t bg) {
    if (c < 32 || c > 127) c = '?';
    const uint8_t* glyph = font8x8[(unsigned char)c - 32];
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            uint8_t pix = (bits >> (7-col)) & 1 ? fg : bg;
            if (bg == COL_TRANSPARENT && !((bits >> (7-col)) & 1)) continue;
            gfx_putpixel(x+col, y+row, pix);
        }
    }
}

void gfx_puts(int x, int y, const char* s, uint8_t fg, uint8_t bg) {
    for (; *s; s++, x+=8)
        gfx_putchar(x, y, *s, fg, bg);
}

void gfx_puts_centered(int y, const char* s, uint8_t fg, uint8_t bg) {
    int len=0; for (const char*p=s;*p;p++) len++;
    int x = (SCREEN_W - len*8) / 2;
    gfx_puts(x, y, s, fg, bg);
}

